public class Calculadora {
 public double somar(double A,double B){ 
 double result = A + B;    
 return result ;
 } 
public double subtracao(double A,double B){
    double result = A - B;    
    return result ;
 }
 public double divisao(double A,double B){
    double result = A / B;    
    return result ;
 } 
 public double multiplicacao(double A,double B){
    double result = A * B;    
    return result ;
 }
}